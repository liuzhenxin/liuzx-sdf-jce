package org.liuzx.jce.provider.exception;

/**
 * Constants for SDF error codes, based on GM/T 0018-2012.
 */
public interface SDFErrorConstants {
    int SDR_OK = 0x00000000;
    int SDR_BASE = 0x01000000;
    int SDR_UNKNOWERR = SDR_BASE + 0x00000001;
    int SDR_NOTSUPPORT = SDR_BASE + 0x00000002;
    int SDR_COMMFAIL = SDR_BASE + 0x00000003;
    int SDR_HARDFAIL = SDR_BASE + 0x00000004;
    int SDR_OPENDEVICE = SDR_BASE + 0x00000005;
    int SDR_OPENSESSION = SDR_BASE + 0x00000006;
    int SDR_PARDENY = SDR_BASE + 0x00000007;
    int SDR_KEYNOTEXIST = SDR_BASE + 0x00000008;
    int SDR_ALGNOTSUPPORT = SDR_BASE + 0x00000009;
    int SDR_ALGMODNOTSUPPORT = SDR_BASE + 0x0000000A;
    int SDR_PKOPERR = SDR_BASE + 0x0000000B;
    int SDR_SKOPERR = SDR_BASE + 0x0000000C;
    int SDR_SIGNERR = SDR_BASE + 0x0000000D;
    int SDR_VERIFYERR = SDR_BASE + 0x0000000E;
    int SDR_SYMOPERR = SDR_BASE + 0x0000000F;
    int SDR_STEPERR = SDR_BASE + 0x00000010;
    int SDR_FILESIZEERR = SDR_BASE + 0x00000011;
    int SDR_FILENOEXIST = SDR_BASE + 0x00000012;
    int SDR_FILEOFSERR = SDR_BASE + 0x00000013;
    int SDR_KEYTYPEERR = SDR_BASE + 0x00000014;
    int SDR_KEYERR = SDR_BASE + 0x00000015;
    int SDR_ENCDATAERR = SDR_BASE + 0x00000016;
    int SDR_RANDERR = SDR_BASE + 0x00000017;
    int SDR_PRKRERR = SDR_BASE + 0x00000018;
    int SDR_MACERR = SDR_BASE + 0x00000019;
    int SDR_FILEEXISTS = SDR_BASE + 0x0000001A;
    int SDR_FILEWERR = SDR_BASE + 0x0000001B;
    int SDR_NOBUFFER = SDR_BASE + 0x0000001C;
    int SDR_INARGERR = SDR_BASE + 0x0000001D;
    int SDR_OUTARGERR = SDR_BASE + 0x0000001F;
}
